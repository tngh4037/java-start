package variable; // variable 라는 패키지를 만들었다면, 해당 패키지에 들어가는 자바 파일 첫줄에 package variable; 와 같이 소속된 패키지를 선언해주어야 한다.

public class Var1 { // 클래스를 생성할 때는 관례상 첫글자로 대문자를 사용한다.

    public static void main(String[] args) {
        System.out.println(20); // 변경 10 -> 20
        System.out.println(20); // 변경 10 -> 20
        System.out.println(20); // 변경 10 -> 20
    }
}
